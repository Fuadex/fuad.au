// fetch-highlights.js — Phase 2: the recall-deck source. For each visited museum (qid from
// art_data.js), pulls its most famous collection works from Wikidata (P195 = collection,
// P18 image required, ranked by sitelink count = fame proxy). Output: museum_highlights.js
// keyed by museum id. Cache-committed like fetch-art.js; keyless.
//   node fetch-highlights.js [--force]
const fs = require("fs"), path = require("path"), vm = require("vm");
const HERE = __dirname;
const CACHE = path.join(HERE, "highlights_cache.json");
const OUT = path.join(HERE, "museum_highlights.js");
const UA = "fuad.au-canvas/0.1 (https://fuad.au; fuadex@gmail.com)";
const PER_MUSEUM = 40;
const FORCE = process.argv.includes("--force");

const ev = (f, g) => { const c = { window: {} }; vm.createContext(c); vm.runInContext(fs.readFileSync(path.join(HERE, f), "utf8"), c, { filename: f }); return c.window[g]; };
const MUSEUMS = ev("museums.js", "CANVAS_MUSEUMS");
const AD = ev("art_data.js", "CANVAS_ART_DATA");

let cache = {};
try { cache = JSON.parse(fs.readFileSync(CACHE, "utf8")); } catch (e) {}
const sleep = (ms) => new Promise(r => setTimeout(r, ms));
const commonsUrl = (file, w) => "https://commons.wikimedia.org/wiki/Special:FilePath/" + encodeURIComponent(String(file).replace(/ /g, "_")) + "?width=" + w;

async function sparql(q, tries = 3) {
  for (let t = 1; t <= tries; t++) {
    const res = await fetch("https://query.wikidata.org/sparql?query=" + encodeURIComponent(q), {
      headers: { "Accept": "application/sparql-results+json", "User-Agent": UA } });
    if (res.ok) { await sleep(600); return res.json(); }
    if (res.status === 429 || res.status === 503) { await sleep(4000 * t); continue; }
    throw new Error("HTTP " + res.status);
  }
  throw new Error("throttled after retries");
}

async function highlights(mqid) {
  if (!FORCE && cache[mqid]) return cache[mqid];
  const q = `SELECT ?item ?itemLabel ?creatorLabel ?img ?date ?links WHERE {
    ?item wdt:P195 wd:${mqid} ; wdt:P18 ?img ; wikibase:sitelinks ?links .
    FILTER(?links > 0)
    OPTIONAL { ?item wdt:P170 ?creator . }
    OPTIONAL { ?item wdt:P571 ?date . }
    SERVICE wikibase:label { bd:serviceParam wikibase:language "en". }
  } ORDER BY DESC(?links) LIMIT ${PER_MUSEUM * 2}`;
  let rows = [];
  try { rows = (await sparql(q)).results.bindings; }
  catch (e) { console.log(`  ${mqid}: SPARQL failed (${e.message}) — empty deck`); cache[mqid] = []; fs.writeFileSync(CACHE, JSON.stringify(cache)); return []; }
  const seen = {}, out = [];
  for (const b of rows) {
    const qid = b.item.value.split("/").pop();
    if (seen[qid]) continue; seen[qid] = 1;
    const file = decodeURIComponent(b.img.value.split("/").pop());
    out.push({
      qid, title: b.itemLabel.value,
      artist: b.creatorLabel ? b.creatorLabel.value : null,
      year: b.date ? parseInt(b.date.value.slice(0, 4), 10) : null,
      links: parseInt(b.links.value, 10),
      img: commonsUrl(file, 640),
    });
    if (out.length >= PER_MUSEUM) break;
  }
  cache[mqid] = out;
  fs.writeFileSync(CACHE, JSON.stringify(cache));
  return out;
}

(async () => {
  const decks = {};
  for (const m of MUSEUMS) {
    const resolved = AD.museums[m.id];
    if (!resolved || !resolved.qid) { console.log(`- ${m.id}: no resolved qid — skipped`); continue; }
    const deck = await highlights(resolved.qid);
    if (deck.length) decks[m.id] = deck;
    console.log(`- ${m.id} (${resolved.qid}): ${deck.length} works`);
  }
  const header = "// GENERATED by fetch-highlights.js — per-museum recall decks (fame-ranked collection\n// works from Wikidata). Do not hand-edit.\n";
  fs.writeFileSync(OUT, header + "window.CANVAS_HIGHLIGHTS = " + JSON.stringify(decks) + ";\n");
  const total = Object.values(decks).reduce((s, d) => s + d.length, 0);
  console.log(`\nmuseum_highlights.js written: ${Object.keys(decks).length} decks · ${total} works`);
})().catch(e => { console.error("FATAL", e.message); process.exit(1); });
